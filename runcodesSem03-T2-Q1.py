

PI = 3.141592

raio = float(input())

comprimento_circunferencia = 2 * PI * raio

area_circulo = PI * (raio ** 2)

area_esfera = 4* PI * (raio **2)

volume_esfera =  4/3 * PI * (raio ** 3)

print("%.6f" %(comprimento_circunferencia))
print("%.6f" %(area_circulo ))
print("%.6f" %(area_esfera ))
print("%.6f" %(volume_esfera ))


